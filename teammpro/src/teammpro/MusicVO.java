package teammpro;

public class MusicVO {
	public String title;
	public String path;
	
	public MusicVO(String title, String path) {
		super();
		this.title = title;
		this.path = path;
	}

	public String getTitle() {
		return title;
	}

	public String getPath() {
		return path;
	}
	
}
